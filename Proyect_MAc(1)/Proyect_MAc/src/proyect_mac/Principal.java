    public static void cambiarTP1(JTextPane jtxpane) {
        String text = jtxpane.getText();
        String[] words = text.split("\\s+");

        DefaultStyledDocument doc = new DefaultStyledDocument();
        SimpleAttributeSet standard = new SimpleAttributeSet();
        StyleConstants.setForeground(standard, jtxpane.getForeground());

        for (String word : words) {
            if (word.equalsIgnoreCase("CREATETABLE")) {
                SimpleAttributeSet colored = new SimpleAttributeSet();
                StyleConstants.setForeground(colored, java.awt.Color.BLUE);
                try {
                    doc.insertString(doc.getLength(), word.toUpperCase(), colored);
                    doc.insertString(doc.getLength(), " ", standard);

                } catch (BadLocationException e) {
                    e.printStackTrace();
                }
            } else {
                try {
                    doc.insertString(doc.getLength(), word, standard);
                    doc.insertString(doc.getLength(), " ", standard);
                } catch (BadLocationException e) {
                    e.printStackTrace();
                }
            }
        }

        jtxpane.setDocument(doc);
    }

